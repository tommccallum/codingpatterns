package commandpattern;

/**
 * Command holds an instance of the object it is sending the command to.
 * @author Tom McCallum
 */
public class LightOnCommand implements Command {
    Light light;
    
    public LightOnCommand(Light light) {
        this.light = light;
    }
    
    @Override
    public void execute() {
        light.on();
    }
    
}
